char	*ft_strrchr(const char *s, int c)
{
	char	*p;
	
	p = 0;
	while (*s != 0)
	{
		if (*s == c)
			p = (char *)s;
		s++;
	}
	if (c == 0)
		p = (char *)s;	
	if (p == 0)
		return (0);
	return (p);
}
